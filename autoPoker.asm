const CARDS 1
const LIM 2

ADDI LIM 4 LIM

label ROUND
XOR CARDS CARDS CARDS
ADD INP 0 CARDS
LTE CARDS LIM WIN
LTEI CARDS 7 ALSO_WIN
SUB CARDS LIM 3
GTEI 3 3 S_CARDS

label WIN
SUBI CARDS 1 CARDS
ADD 0 CARDS OUT

label ALSO_WIN
SUBI CARDS 5 CARDS
ADD CARDS 0 OUT
JMP 0 0 ROUND

label S_CARDS
ADDI 0 3 OUT
JMP 0 0 ROUND
