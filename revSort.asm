const I 3
const MAX 4
const REG0 0
const REG1 1
const REG2 2
const J 5

ADDI 0 15 MAX
ADDI 0 254 J

label INPUT
STR I INP 0
ADDI I 1 I
LT I MAX INPUT

SUBI I 1 I

label COMPARE
# CHECK SORTED
ADDI 0 239 REG0
LTE J REG0 END
LDR I 0 REG1
LT REG1 REG2 MOVE_UP
STR J REG1 0 
SUBI I 1 I
SUBI J 1 J
XOR REG2 REG2 REG2
ADDI REG1 0 REG2
JMP 0 0 COMPARE

label MOVE_UP
STR J REG2 0
ADDI J 1 J

label CHECK_BACK
ADDI J 1 J
LDR J 0 REG2
LT REG1 REG2 S_MOVE_UP
SUBI J 1 J
STR J REG1 0
XOR J J J 
SUBI I 1 I
SUB I MAX J
LDR J 0 REG2
SUBI J 1 J
JMP 0 0 COMPARE

label S_MOVE_UP
SUBI J 1 J 
JMP 0 0 MOVE_UP

label END
XOR J J J 
ADDI 254 254 J
JMP 0 0 OUTPUT

label OUTPUT
LDR J 0 OUT
SUBI J 1 J
JMP 0 0 OUTPUT
