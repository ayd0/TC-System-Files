const I 1
const J 2
const DIF 4

STR I INP 0
ADDI I 1 I
ADDI DIF 32 DIF

label LOAD_I
STR I INP 0
#	CHECK ENDSTR
LDR I 0 3
EQ 3 0 S_END_STR
#	INCR COUNTERS
ADDI I 1 I
ADDI J 1 J
JMP 0 0 LOAD_I

label S_END_STR
XOR I I I 
JMP 0 0 END_STR

label S_F_END_STR
# 	SPACE OUT
LDR I 0 INP
ADDI I 1 I

label END_STR 
LDR I 0 3
SUB 3 DIF 3
ADDI 3 0 OUT

label F_END_STR
ADDI I 1 I
LDR I 0 3
EQ 3 DIF S_F_END_STR
EQ 3 0 LOAD_I
ADDI 3 0 OUT
JMP 0 0 F_END_STR
